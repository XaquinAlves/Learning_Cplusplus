#include <iostream>
using namespace std;

int main(){
    int num1=10, num2=2;

    cout << "Para " << num1 << " y " << num2 << endl;
    cout << "Suma: " << num1+num2 << endl;
    cout << "Resta: " << num1-num2 << endl;
    cout << "Multiplicacion: " << num1*num2 << endl;
    cout << "Division: " << num1/num2 << endl;
    cout << "Resto: " << num1%num2 << endl;
    cout << "Incremento para " << num1 << " : " << ++num1 << endl;
    cout << "Decremento para " << num2 << " : " << --num2 << endl;

    return 0; 
}